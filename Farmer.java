public class Farmer {

    private String name;
    private int age;
    private String type; 

    public void setName(String n) {
        name = n;
    }
    public String getName() {
        return name;
    }

    public void setAge(int a) {
        age = a;
    }
    public int getAge() {
        return age;
    }

    public void setType(String t) {
        type = t;
    }
    public String getType() {
        return type;
    }


}